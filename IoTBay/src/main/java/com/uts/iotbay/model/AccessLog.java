package com.uts.iotbay.model;

public class AccessLog {
     private int id;
    private int userId;
    private String time;

    public AccessLog(int id, int userId, String time) {
        this.id = id;
        this.userId = userId;
        this.time = time;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
